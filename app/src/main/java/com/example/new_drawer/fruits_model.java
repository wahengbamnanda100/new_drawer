package com.example.new_drawer;

public class fruits_model {

    String Fruit_name;
    String Fruit_country;
    String Fruit_net;
    String price1;
    String price2;

    public String getFruit_name() {
        return Fruit_name;
    }

    public void setFruit_name(String fruit_name) {
        Fruit_name = fruit_name;
    }

    public String getFruit_country() {
        return Fruit_country;
    }

    public void setFruit_country(String fruit_country) {
        Fruit_country = fruit_country;
    }

    public String getFruit_net() {
        return Fruit_net;
    }

    public void setFruit_net(String fruit_net) {
        Fruit_net = fruit_net;
    }

    public String getPrice1() {
        return price1;
    }

    public void setPrice1(String price1) {
        this.price1 = price1;
    }

    public String getPrice2() {
        return price2;
    }

    public void setPrice2(String price2) {
        this.price2 = price2;
    }
}
